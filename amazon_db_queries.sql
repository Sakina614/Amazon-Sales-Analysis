    ss.seller_id,
    ss.order_date AS last_sale_date,
    ss.total_price AS last_sale_amount
FROM seller_sales ss
JOIN inactive_sellers i
    ON ss.seller_id = i.seller_id
WHERE ss.rn = 1;

