    JOIN orders o
        ON oi.order_id = o.order_id
),
inactive_sellers AS (
    SELECT seller_id
    FROM seller_sales
    GROUP BY seller_id
    HAVING MAX(order_date) < CURRENT_DATE - INTERVAL '1 month'
)
