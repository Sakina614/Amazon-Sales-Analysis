WITH top_sellers AS
(
    SELECT
        s.seller_id,
        s.seller_name,
        SUM(oi.total_price) AS total_sales
    FROM sellers s
    JOIN products p
        ON p.seller_id = s.seller_id
    JOIN order_items oi
        ON oi.product_id = p.product_id
    GROUP BY
        s.seller_id,
        s.seller_name
    ORDER BY total_sales DESC
    LIMIT 5
), seller_orders AS
(
    SELECT
        ts.seller_id,
        ts.seller_name,
        o.order_status,
        COUNT(DISTINCT o.order_id) AS total_orders
    FROM top_sellers ts
    JOIN products p
        ON p.seller_id = ts.seller_id
    JOIN order_items oi
        ON oi.product_id = p.product_id
    JOIN orders o
        ON o.order_id = oi.order_id
    WHERE o.order_status NOT IN ('Pending','Returned','Shipped')
    GROUP BY
        ts.seller_id,
        ts.seller_name,
        o.order_status
)
SELECT
    seller_id,
    seller_name,

    SUM(
        CASE
            WHEN order_status='Delivered'
            THEN total_orders
            ELSE 0
        END
    ) AS completed_orders,

    SUM(total_orders) AS total_orders,

    ROUND(
        SUM(
            CASE
                WHEN order_status='Delivered'
                THEN total_orders
                ELSE 0
            END
        )::numeric
        /
        SUM(total_orders)
        *100,
        2
    ) AS success_percentage

FROM seller_orders
GROUP BY
    seller_id,
    seller_name
	ORDER BY success_percentage DESC;
	
